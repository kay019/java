package ch02;

public class VarEx01 {
    public static void main(String[] args){

        System.out.println("6 + 3");
        System.out.println(6 + 3);
        System.out.println(6 - 3);
        System.out.println(6 * 3);
        System.out.println(6 / 3);

        int x = 4, y = 2;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

    }
}
